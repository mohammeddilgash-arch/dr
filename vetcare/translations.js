window.translations = translations;

const SUPPORTED_LANGS = ['en', 'ar', 'ku'];

function normalizeLang(lang) {
    return SUPPORTED_LANGS.includes(lang) ? lang : 'en';
}

function getStoredLang() {
    try {
        return normalizeLang(localStorage.getItem('vetcare-lang') || 'en');
    } catch (_error) {
        return 'en';
    }
}

function saveStoredLang(lang) {
    try {
        localStorage.setItem('vetcare-lang', normalizeLang(lang));
    } catch (_error) {
        // Ignore storage failures (private browsing, blocked storage).
    }
}

function getFontHref(lang) {
    if (lang === 'ar') {
        return 'https://fonts.googleapis.com/css2?family=Tajawal:wght@400;500;600;700&family=El+Messiri:wght@400;500;600;700&display=swap';
    }
    if (lang === 'ku') {
        return 'https://fonts.googleapis.com/css2?family=Noto+Sans+Arabic+KU:wght@400;500;600;700&family=Noto+Sans:wght@400;500;600;700&display=swap';
    }
    return 'https://fonts.googleapis.com/css2?family=Syne:wght@500;600;700;800&family=Manrope:wght@400;500;600;700&display=swap';
}

function applyLanguagePresentation(lang) {
    const isRTL = lang === 'ar' || lang === 'ku';
    document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;

    const fontLink = document.getElementById('dynamic-font') || document.createElement('link');
    fontLink.id = 'dynamic-font';
    fontLink.rel = 'stylesheet';
    if (!document.getElementById('dynamic-font')) {
        document.head.appendChild(fontLink);
    }
    fontLink.href = getFontHref(lang);
}

function getTranslationValue(root, key) {
    return key.split('.').reduce((acc, part) => (acc ? acc[part] : undefined), root);
}

let currentLang = getStoredLang();

function setLanguage(lang) {
    currentLang = normalizeLang(lang);
    saveStoredLang(currentLang);
    applyLanguagePresentation(currentLang);
    translatePage();
    updateActiveLanguage();
}

window.setLanguage = setLanguage;

function translatePage() {
    const t = translations[currentLang] || translations.en;
    const page = getCurrentPage();

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const value = key ? getTranslationValue(t, key) : undefined;

        if (value) {
            if (el.tagName === 'OPTION') {
                el.textContent = value;
            } else {
                el.innerHTML = value;
            }
        }
    });
    
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        const value = key ? getTranslationValue(t, key) : undefined;

        if (value) {
            el.placeholder = value;
        }
    });
    
    document.title = getPageTitle(page);
}

window.translatePage = translatePage;

function getCurrentPage() {
    const path = window.location.pathname;
    if (path.includes('services')) return 'services';
    if (path.includes('appointments')) return 'appointments';
    if (path.includes('contact')) return 'contact';
    return 'home';
}

function getPageTitle(page) {
    const titles = {
        home: currentLang === 'ar' ? 'عيادة غولان البيطرية | رعاية رحيمة' : 
              currentLang === 'ku' ? 'کلینیکی گوڵان | چاودێریی میهرەبانانە' :
              'Gullan | Veterinary Excellence',
        services: currentLang === 'ar' ? 'خدماتنا | عيادة غولان البيطرية' :
                  currentLang === 'ku' ? 'خزمەتەکان | کلینیکی گوڵان' :
                  'Services | Gullan Veterinary Clinic',
        appointments: currentLang === 'ar' ? 'حجز موعد | غولان' :
                       currentLang === 'ku' ? 'کات دابنێ | گوڵان' :
                       'Book Appointment | Gullan',
        contact: currentLang === 'ar' ? 'اتصل بنا | عيادة غولان البيطرية' :
                 currentLang === 'ku' ? 'پەیوەندی | کلینیکی گوڵان' :
                 'Contact Us | Gullan Veterinary Clinic'
    };
    return titles[page] || titles.home;
}

function updateActiveLanguage() {
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('bg-fresh-green', 'text-white');
        btn.classList.add('text-on-surface-variant', 'hover:text-fresh-green');
        if (btn.getAttribute('data-lang') === currentLang) {
            btn.classList.add('bg-fresh-green', 'text-white');
            btn.classList.remove('text-on-surface-variant', 'hover:text-fresh-green');
        }
    });
}

function initLanguage() {
    currentLang = getStoredLang();
    applyLanguagePresentation(currentLang);
    translatePage();
    updateActiveLanguage();
}

if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initLanguage);
} else {
    initLanguage();
}